CREATE TABLE ADMINS (
ADMIN_ID VARCHAR2(10) NOT NULL
CONSTRAINT admin_id_pk PRIMARY KEY,
ADMIN_NAME VARCHAR2(32) NOT NULL,
ADMIN_PASSWORD VARCHAR2(20) NOT NULL,
ADMIN_AGE NUMBER(4),
ADMIN_PHONE NUMBER(6),
ACCOUNT_CREATE_DATE DATE DEFAULT SYSDATE
);
CREATE TABLE ADMIN_ADDRESS(
ADMIN_ID VARCHAR2(10) NOT NULL ,
ADMIN_STATE VARCHAR2(32) NOT NULL,
ADMIN_CITY VARCHAR2(20) NOT NULL,
ADMIN_ZIP NUMBER(4),
CONSTRAINT ADMIN_ADDRESS_admin_id_fk FOREIGN KEY(ADMIN_ID) REFERENCES ADMINS(ADMIN_ID)
);

CREATE TABLE DEPARTMENT( 
Dept_no VARCHAR2(4) NOT NULL CONSTRAINT DEPARTMENT_Dept_no_PK PRIMARY KEY, 
Dept_name VARCHAR2(20) NOT NULL, 
Dept_location VARCHAR2(20)
);
CREATE TABLE EMPLOYEES_SHIFT( 
EMP_SHIFT_ID VARCHAR2(4) NOT NULL CONSTRAINT EMP_SHIFT_emp_shift_id_PK PRIMARY KEY, 
SHIFT_NAME VARCHAR2(20) NOT NULL
);
CREATE TABLE EMPLOYEES(
EMPLOYEE_NUMBER NUMBER(3) NOT NULL CONSTRAINT employee_id_pk PRIMARY KEY,
Employee_Name VARCHAR2(32) NOT NULL,
EMPLOYEE_PHONE NUMBER(3),
EMPLOYEE_SALARY NUMBER(6) NOT NULL,
EMPLOYEE_COMM NUMBER(4),
EMPLOYEE_AGE NUMBER(2) NOT NULL,
HIREDATE DATE NOT NULL,
EMPLOYEE_DEPT VARCHAR2(10) NOT NULL,
EMP_SHIFT_ID VARCHAR2(4) NOT NULL,
CONSTRAINT EMPLOYEE_DEPT_fk FOREIGN KEY(EMPLOYEE_DEPT) REFERENCES DEPARTMENT(DEPT_NO),
CONSTRAINT EMPLOYEES_SHIFT_ID_FK FOREIGN KEY (EMP_SHIFT_ID) REFERENCES EMPLOYEES_SHIFT (EMP_SHIFT_ID)
);
CREATE TABLE EMPLOYEES_ADDRESS(
EMPLOYEE_NUMBER NUMBER(20) NOT NULL ,
EMPLOYEES_STATE VARCHAR2(32) NOT NULL,
EMPLOYEES_CITY VARCHAR2(20) NOT NULL,
EMPLOYEES_ZIP NUMBER(4),
CONSTRAINT EMPLOYEES_ADDRESS_EMPLOYEES_NUMBER_fk FOREIGN KEY(EMPLOYEE_NUMBER) REFERENCES EMPLOYEES(EMPLOYEE_NUMBER)
);



CREATE TABLE EMPLOYEES_ATTENDANCE( 
EMPLOYEE_NUMBER NUMBER(4) NOT NULL, 
WORKING_DATE DATE DEFAULT SYSDATE, 
ATTENDANCE_STATUS VARCHAR2(10), 
CONSTRAINT EMPLOYEES_ATTENDANCE_Emp_no_FK FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEES (EMPLOYEE_NUMBER),  
CONSTRAINT ck_Attendance_status CHECk (Attendance_status IN ('P','A','L')) 
 
);