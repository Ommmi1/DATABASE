SELECT "EMPLOYEENAME" FROM (SELECT FIRSTNAME || ' ' || LASTNAME AS "EMPLOYEENAME", COUNT(ORDERID) FROM EMPLOYEES 
INNER JOIN ORDERS ON EMPLOYEES.EMPLOYEEID = ORDERS.EMPLOYEEID GROUP BY FIRSTNAME || ' ' || LASTNAME 
ORDER BY COUNT(ORDERID) DESC) WHERE ROWNUM <= 3;

Select od.orderid, p.productname,ca.categoryname 
from OrderDetails od inner join Products p on od.productid=p.productid inner join Orders o 
on od.orderid=o.orderid inner join Categories ca on p.categoryid=ca.categoryid 
where ca.categoryname not in ('Meat/Poultry','Dairy Products') ;

select o.orderid,sum((od.unitprice*od.quantity)-discount) as "Total Price" 
from Orders o inner join Orderdetails od on o.orderid=od.orderid 
group by o.orderid;

