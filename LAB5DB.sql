from Suppliers Inner Join Products On Suppliers.SupplierID = Products.SupplierID
Group By Suppliers.ContactName having count(Products.ProductID) > 4;

SELECT REGION.REGIONDESCRIPTION AS REGIONNAME, COUNT(EMPLOYEES.EMPLOYEEID) AS NO_OF_EMPLOYEES FROM REGION
INNER JOIN TERRITORIES ON TERRITORIES.REGIONID = REGION.REGIONID 
INNER JOIN EMPLOYEETERRITORIES ON EMPLOYEETERRITORIES.TERRITORYID = TERRITORIES.TERRITORYID 
INNER JOIN EMPLOYEES ON EMPLOYEES.EMPLOYEEID = EMPLOYEETERRITORIES.EMPLOYEEID 
GROUP BY REGION.REGIONDESCRIPTION;

